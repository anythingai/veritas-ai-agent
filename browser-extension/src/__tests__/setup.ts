import '@testing-library/jest-dom';
import { vi } from 'vitest';

// Mock Chrome API for browser extension tests
global.chrome = {
  runtime: {
    sendMessage: vi.fn(),
    onMessage: {
      addListener: vi.fn(),
      removeListener: vi.fn()
    }
  },
  storage: {
    local: {
      get: vi.fn(),
      set: vi.fn(),
      remove: vi.fn()
    }
  },
  tabs: {
    query: vi.fn(),
    sendMessage: vi.fn()
  }
} as any;

// Mock fetch for API calls
global.fetch = vi.fn();

// Mock window.matchMedia
